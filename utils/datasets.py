        # Convert
        img = img.transpose((2, 0, 1))[::-1]  # HWC to CHW, BGR to RGB
        img = np.ascontiguousarray(img)

        # Return ship corners as an additional output
        return torch.from_numpy(img), labels_out, self.img_files[index], shapes, ship_corners

